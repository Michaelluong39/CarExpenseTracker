package com.company.models;

public class maintenance {
    private float maintCost;
    private String maintName;
    private int mainYear;
    private int mainMonth;
    private int mainDay;

    public float getMaintCost() {
        return maintCost;
    }

    public void setMaintCost(float maintCost) {
        this.maintCost = maintCost;
    }

    public String getMaintName() {
        return maintName;
    }

    public void setMaintName(String maintName) {
        this.maintName = maintName;
    }

    public int getMainYear() {
        return mainYear;
    }

    public void setMainYear(int mainYear) {
        this.mainYear = mainYear;
    }

    public int getMainMonth() {
        return mainMonth;
    }

    public void setMainMonth(int mainMonth) {
        this.mainMonth = mainMonth;
    }

    public int getMainDay() {
        return mainDay;
    }

    public void setMainDay(int mainDay) {
        this.mainDay = mainDay;
    }
}
